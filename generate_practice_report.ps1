function Log-Step {
    param([string]$Message)
    if ($script:LogPath) {
        $timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
        Add-Content -Path $script:LogPath -Value "$timestamp`t$Message" -Encoding UTF8
    }
}

function Set-DefaultStyle {
    param($Document)
    $style = $Document.Styles.Item(-1)
    $style.Font.Name = "Times New Roman"
    $style.Font.Size = 14
}

function Add-ParagraphText {
    param(
        $Selection,
        [string]$Text,
        [int]$Align = 0,
        [int]$Size = 14,
        [string]$FontName = "Times New Roman",
        [bool]$Bold = $false
    )
    $Selection.ParagraphFormat.Alignment = $Align
    $Selection.Font.Name = $FontName
    $Selection.Font.Size = $Size
    $Selection.Font.Bold = [int]$Bold
    $Selection.TypeText($Text)
    $Selection.TypeParagraph()
    $Selection.Font.Bold = 0
}

function Add-BlankLines {
    param($Selection, [int]$Count)
    for ($i = 0; $i -lt $Count; $i++) {
        $Selection.TypeParagraph()
    }
}

function Add-Heading {
    param($Selection, [string]$Text, [int]$Level = 1)
    $styleId = if ($Level -eq 1) { -2 } else { -3 }
    $Selection.Style = $styleId
    $Selection.Font.Name = "Times New Roman"
    $Selection.Font.Size = if ($Level -eq 1) { 16 } else { 14 }
    $Selection.Font.Bold = 1
    $Selection.ParagraphFormat.Alignment = 0
    $Selection.TypeText($Text)
    $Selection.TypeParagraph()
    $Selection.Style = -1
    $Selection.Font.Bold = 0
}

function Add-BulletList {
    param($Selection, [string[]]$Items)
    foreach ($item in $Items) {
        Add-ParagraphText -Selection $Selection -Text ("- " + $item) -Align 0 -Size 14
    }
}

function Add-MultiParagraph {
    param($Selection, [string]$Text)
    $parts = $Text -split "(`r`n){2,}|(`n){2,}"
    foreach ($part in $parts) {
        $trimmed = $part.Trim()
        if ($trimmed) {
            Add-ParagraphText -Selection $Selection -Text $trimmed -Align 3 -Size 14
        }
    }
}

function Add-TableFromRows {
    param($Selection, [object[][]]$Rows)
    $range = $Selection.Range
    $table = $Selection.Tables.Add($range, $Rows.Count, $Rows[0].Count)
    $table.Borders.Enable = 1
    for ($r = 1; $r -le $Rows.Count; $r++) {
        for ($c = 1; $c -le $Rows[$r - 1].Count; $c++) {
            $cellRange = $table.Cell($r, $c).Range
            $cellRange.Text = [string]$Rows[$r - 1][$c - 1]
            $cellRange.Font.Name = "Times New Roman"
            $cellRange.Font.Size = 12
        }
    }
    $Selection.MoveDown()
    $Selection.TypeParagraph()
}

function Add-CodeAppendix {
    param(
        $Selection,
        [string]$Title,
        [string]$Path,
        [int]$MaxChars = 8000
    )
    $Selection.InsertBreak(7)
    Add-Heading -Selection $Selection -Text $Title -Level 1
    Add-ParagraphText -Selection $Selection -Text ("Файл: " + $Path) -Align 0 -Size 12
    if (-not (Test-Path $Path)) {
        Add-ParagraphText -Selection $Selection -Text "Файл отсутствует в текущей версии проекта." -Align 0 -Size 12
        return
    }
    $content = Get-Content -Path $Path -Raw -Encoding UTF8
    if ($MaxChars -gt 0 -and $content.Length -gt $MaxChars) {
        $content = $content.Substring(0, $MaxChars) + "`r`n... [содержимое сокращено для отчёта]"
    }
    $Selection.Font.Name = "Consolas"
    $Selection.Font.Size = 9
    $Selection.ParagraphFormat.Alignment = 0
    $Selection.TypeText($content)
    $Selection.TypeParagraph()
    $Selection.Font.Name = "Times New Roman"
    $Selection.Font.Size = 14
}

function Repair-NetworkProject {
    param(
        [string]$LegacyPath,
        [string]$SavedCoordsPath,
        [string]$OutputPath
    )

    $legacy = Get-Content -Path $LegacyPath -Raw -Encoding UTF8 | ConvertFrom-Json
    $saved = Get-Content -Path $SavedCoordsPath -Raw -Encoding UTF8 | ConvertFrom-Json
    $current = $null
    if (Test-Path $OutputPath) {
        $current = Get-Content -Path $OutputPath -Raw -Encoding UTF8 | ConvertFrom-Json
    }

    $linksById = @{}
    if ($current -and $current.network -and $current.network.links) {
        foreach ($link in $current.network.links) {
            $linksById[$link.id] = $link
        }
    }

    $coordToNode = @{}
    $nodes = New-Object System.Collections.ArrayList
    function Get-NodeId([double]$lon, [double]$lat) {
        $key = "{0:F6}|{1:F6}" -f $lon, $lat
        if (-not $coordToNode.ContainsKey($key)) {
            $nodeId = "N{0}" -f ($coordToNode.Count + 1)
            $coordToNode[$key] = $nodeId
            [void]$nodes.Add([ordered]@{
                id = $nodeId
                lon = [math]::Round($lon, 6)
                lat = [math]::Round($lat, 6)
                x = $null
                y = $null
                node_type = "intersection"
                name = $nodeId
                metadata = @{}
            })
        }
        return $coordToNode[$key]
    }

    $outLinks = New-Object System.Collections.ArrayList
    foreach ($legacyLink in $legacy.directional_links) {
        if (-not $saved.PSObject.Properties.Name.Contains($legacyLink.id)) {
            continue
        }
        $coords = $saved.$($legacyLink.id)
        $startNodeId = Get-NodeId $coords.lon_start $coords.lat_start
        $endNodeId = Get-NodeId $coords.lon_end $coords.lat_end

        if ($linksById.ContainsKey($legacyLink.id)) {
            $linkObj = $linksById[$legacyLink.id]
            $parameters = $linkObj.parameters
            $results = $linkObj.results
            $metadata = $linkObj.metadata
        } else {
            $parameterNames = @(
                "lanes_total","lanes_bus","capacity_per_lane_base","lane_width_m","grade_percent",
                "parking_present","heavy_vehicles_percent","cycle_time","green_time","g_others",
                "saturation_flow_base","lanes_count","is_ring_approach"
            )
            $parameters = [ordered]@{}
            foreach ($name in $parameterNames) {
                if ($legacyLink.PSObject.Properties.Name -contains $name) {
                    $parameters[$name] = $legacyLink.$name
                }
            }
            $results = @{}
            $metadata = @{}
        }

        [void]$outLinks.Add([ordered]@{
            id = $legacyLink.id
            name = $legacyLink.name
            start_node_id = $startNodeId
            end_node_id = $endNodeId
            link_type = $legacyLink.type
            length_km = $legacyLink.length_km
            traffic_counts = $legacyLink.traffic_counts
            coords = [ordered]@{
                lon_start = [math]::Round($coords.lon_start, 6)
                lat_start = [math]::Round($coords.lat_start, 6)
                lon_end = [math]::Round($coords.lon_end, 6)
                lat_end = [math]::Round($coords.lat_end, 6)
            }
            parameters = $parameters
            results = $results
            metadata = $metadata
        })
    }

    $routes = New-Object System.Collections.ArrayList
    foreach ($route in $legacy.routes) {
        [void]$routes.Add([ordered]@{
            id = $route.id
            name = $route.name
            link_ids = @($route.links)
            results = @{}
        })
    }

    $project = [ordered]@{
        project_name = $legacy.project_name
        pcu_coefficients = $legacy.pcu_coefficients
        metadata = [ordered]@{
            source_format = "legacy"
            repaired_for_report = $true
        }
        network = [ordered]@{
            nodes = $nodes
            links = $outLinks
            routes = $routes
        }
        scenarios = @()
    }

    $json = $project | ConvertTo-Json -Depth 20
    Set-Content -Path $OutputPath -Value $json -Encoding UTF8
}

